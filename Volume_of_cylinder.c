//Volume of cylinder
#include <stdio.h>

int main() 
{
 double PI , radius , height, volume_of_cylinder;
     PI = 3.142;
     printf("Enter the radius :");
     scanf("%lf",&radius);

     printf("Enter the height :");
     scanf("%lf",&height);
     
     volume_of_cylinder = PI * radius * radius * height;
     printf(" The volume of cylinder is %.2lf centimetre cubed",volume_of_cylinder);

    return 0;
}